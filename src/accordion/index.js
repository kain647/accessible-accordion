const Expandable = props => {
  const { content, name, isCollapsed = true, duration = 300 } = props;
  return (
    <SlideToggle
      collapsed={isCollapsed}
      duration={duration}
      render={({ toggle, setCollapsibleElement, progress }) => {
        const expanded = progress === 1;
        const icon = expanded ? <FaMinusCircle /> : <FaPlusCircle />;
        return (
          <Block>
            <BlockControl onClick={toggle}>
              {icon}
              {name}
            </BlockControl>
            <BlockContent ref={setCollapsibleElement}>{content}</BlockContent>
          </Block>
        );
      }}
    />
  );
};

